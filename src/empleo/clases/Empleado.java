public class Empleado {
    String nombre;
    String apellidoPaterno;
    String apellidoMaterno;
    String area;
    int edad;
    
    public Empleado(String nombre, String apellidoPaterno,String apellidoMaterno,String area, int edad){
        this.nombre=nombre;
        this.apellidoPaterno=apellidoPaterno;
        this.apellidoMaterno=apellidoMaterno;
        this.area=area;
        this.edad=edad;
    }
    
   
    
    
}
